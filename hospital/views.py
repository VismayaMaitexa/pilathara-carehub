from django.shortcuts import render, redirect
from django.core.files.storage import FileSystemStorage
from .model import anemia_prediction_pipeline, preprocess_features, predict_anemia, extract_text_from_image


def upload_image(request):
    if request.method == 'POST' and request.FILES.get('file'):
        file = request.FILES['file']
        fs = FileSystemStorage()
        filename = fs.save(file.name, file)
        file_url = fs.url(filename)

        # Extract text from the image
        extracted_text = extract_text_from_image(fs.path(filename))

        # If no text is extracted (None or empty), redirect to manual entry instead of error
        if not extracted_text or not extracted_text.strip():
            return render(request, 'hospital/manual_entry.html', {'missing_values': ['Gender', 'Hemoglobin', 'MCV', 'MCH', 'MCHC']})

        # Extract values and check for missing data
        result, missing_values = anemia_prediction_pipeline(fs.path(filename))

        # If values are missing, go to manual entry page
        if missing_values:
            return render(request, 'hospital/manual_entry.html', {'missing_values': missing_values})

        return render(request, 'hospital/result.html', {'result': result[0], 'anemia_detected': result[1], 'file_url': file_url})

    return render(request, 'hospital/upload.html')


