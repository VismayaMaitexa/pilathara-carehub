def pharmacy_login(request):
    if request.method == 'POST':
        username = request.POST.get('username')
        password = request.POST.get('password')

        # Check if the provided username and password match the principal's credentials
        if username == 'pharmacy' and password == '1234':
            # Check if the user with the specified username exists
            user = User.objects.filter(username=username).first()

            if not user:
                # If the user doesn't exist, create one
                user = User.objects.create_user(username=username, password=password)

            # Log in the user
            login(request, user)
            return redirect('pharmacy-home')
        else:
            # Invalid login credentials
            return render(request, "hospital/pharmacylogin.html", {'error_message': 'Invalid username or password'})

    return render(request, "hospital/pharmacylogin.html")



def pharmacy_home(request):
    customercount = models.Patient.objects.all().count()
    productcount = models.Product.objects.all().count()
    ordercount = models.Orders.objects.all().count()

    orders = models.Orders.objects.all().order_by('-id')
    ordered_products = []
    ordered_bys = []

    for order in orders:
        ordered_product = models.Product.objects.filter(id=order.product.id).first()
        if order.patient:
            ordered_by = models.Patient.objects.filter(id=order.id).first()
        else:
            ordered_by = None
        
        ordered_products.append(ordered_product)
        ordered_bys.append(ordered_by)

    mydict = {
        'customercount': customercount,
        'productcount': productcount,
        'ordercount': ordercount,
        'data': zip(ordered_products, ordered_bys, orders),
    }
    
    return render(request, 'hospital/pharmacy_home.html', context=mydict)





